import React, { useState, useMemo } from 'react';
import ScoreRing from '../components/ScoreRing';
import CategoryBar from '../components/CategoryBar';
import VulnCard from '../components/VulnCard';

const SEV_ORDER = { CRITICAL: 0, HIGH: 1, MEDIUM: 2, LOW: 3 };

const CAT_ICONS = {
  'Transport Security':    '🔒',
  'HTTP Headers':          '📋',
  'CORS & API Security':   '🌐',
  'Cookie & Session':      '🍪',
  'File & Info Exposure':  '📁',
  'DNS & Network':         '🌍',
  'Rate Limiting & Abuse': '⚡',
};

const CAT_WEIGHTS = {
  'Transport Security':    45,
  'HTTP Headers':          29,
  'CORS & API Security':   26,
  'Cookie & Session':      11,
  'File & Info Exposure':  21,
  'DNS & Network':         7,
  'Rate Limiting & Abuse': 10,
};

export default function Results({ results, finalData, url, onRescan }) {
  const [filter, setFilter] = useState('all');

  const sorted = useMemo(() => {
    if (!results) return [];
    return [...results].sort((a, b) => {
      const ao = a.status === 'PASS' || a.status === 'ERROR' ? 5 : (SEV_ORDER[a.severity] ?? 4);
      const bo = b.status === 'PASS' || b.status === 'ERROR' ? 5 : (SEV_ORDER[b.severity] ?? 4);
      return ao - bo;
    });
  }, [results]);

  const filtered = useMemo(() => {
    if (filter === 'issues') return sorted.filter(r => r.status === 'FAIL' || r.status === 'WARNING');
    if (filter === 'pass')   return sorted.filter(r => r.status === 'PASS');
    if (filter === 'critical') return sorted.filter(r => r.severity === 'CRITICAL' && r.status === 'FAIL');
    return sorted;
  }, [sorted, filter]);

  if (!finalData) return null;

  const { score, grade, label, summary, categoryScores, topAttackVectors, scanDuration, hostname } = finalData;

  const scoreColor = score >= 75 ? '#16a34a' : score >= 55 ? '#d97706' : '#e02929';

  const generateReport = () => {
    const lines = [
      '=== ShieldScan Security Report ===',
      `Target: ${url}`,
      `Date:   ${new Date().toLocaleString()}`,
      `Score:  ${score}/100  Grade: ${grade}  (${label})`,
      '',
      '--- Summary ---',
      `Critical: ${summary.critical}  High: ${summary.high}  Medium: ${summary.medium}  Low: ${summary.low}  Pass: ${summary.pass}`,
      '',
      '--- Category Scores ---',
      ...Object.entries(categoryScores).map(([cat, sc]) => `${cat}: ${sc}%`),
      '',
      '--- Findings ---',
      '',
    ];
    results.forEach((r, i) => {
      lines.push(`[${i+1}] ${r.name}`);
      lines.push(`  Status:   ${r.status}`);
      if (r.status !== 'PASS' && r.status !== 'ERROR') lines.push(`  Severity: ${r.severity} (-${r.points_deducted} pts)`);
      lines.push(`  Finding:  ${r.description}`);
      if (r.technicalDetail) lines.push(`  Detail:   ${r.technicalDetail}`);
      if (r.attackScenario)  lines.push(`  Risk:     ${r.attackScenario}`);
      if (r.fix)             lines.push(`  Fix:      ${r.fix.description}`);
      lines.push('');
    });
    lines.push('Generated by ShieldScan — for authorized testing only.');
    const blob = new Blob([lines.join('\n')], { type: 'text/plain' });
    const a = document.createElement('a');
    a.href = URL.createObjectURL(blob);
    a.download = `shieldscan-${hostname}-${new Date().toISOString().split('T')[0]}.txt`;
    a.click();
  };

  return (
    <div style={{ minHeight: '100vh', background: 'transparent', color: '#1c1c1c', position: 'relative', zIndex: 2 }}>

      {/* Sticky nav */}
      <div style={{ background: '#1c1c1c', padding: '10px 20px', display: 'flex', alignItems: 'center', justifyContent: 'space-between', position: 'sticky', top: 0, zIndex: 100 }}>
        <div style={{ display: 'flex', alignItems: 'center', gap: '8px' }}>
          <span style={{ fontSize: '16px' }}>🛡️</span>
          <span style={{ color: '#fff', fontWeight: 700, fontSize: '15px' }}>ShieldScan</span>
        </div>
        <div style={{ display: 'flex', gap: '8px', alignItems: 'center' }}>
          <span style={{ color: '#888', fontSize: '12px', fontFamily: "'Space Mono', monospace" }}>{hostname}</span>
          <button
            onClick={generateReport}
            style={{ background: '#e02929', color: '#fff', border: 'none', borderRadius: '5px', padding: '6px 14px', fontSize: '13px', fontWeight: 600, cursor: 'pointer' }}
          >
            ↓ Download Report
          </button>
          <button
            onClick={onRescan}
            style={{ background: 'rgba(255,255,255,0.1)', color: '#fff', border: '1px solid rgba(255,255,255,0.2)', borderRadius: '5px', padding: '6px 14px', fontSize: '13px', cursor: 'pointer' }}
          >
            ← Scan another
          </button>
        </div>
      </div>

      <div style={{ maxWidth: '860px', margin: '0 auto', padding: '28px 20px' }}>

        {/* Score section */}
        <div style={{ background: '#fff', border: '1px solid #ddd', borderRadius: '8px', padding: '24px 28px', marginBottom: '16px', display: 'flex', gap: '28px', alignItems: 'center', flexWrap: 'wrap' }}>
          <div style={{ flexShrink: 0, textAlign: 'center' }}>
            <ScoreRing score={score} grade={grade} size={140} />
            <div style={{ marginTop: '6px', fontSize: '13px', fontWeight: 700, color: scoreColor }}>{label}</div>
          </div>
          <div style={{ flex: 1, minWidth: '220px' }}>
            <h2 style={{ fontSize: '18px', fontWeight: 700, marginBottom: '14px', color: '#111' }}>Security Report</h2>
            <div style={{ display: 'flex', gap: '8px', flexWrap: 'wrap', marginBottom: '16px' }}>
              {[
                { n: summary.critical || 0, l: 'Critical', c: '#b91c1c', bg: '#fee2e2' },
                { n: summary.high     || 0, l: 'High',     c: '#e02929', bg: '#fee2e2' },
                { n: summary.medium   || 0, l: 'Medium',   c: '#b45309', bg: '#fef3c7' },
                { n: summary.low      || 0, l: 'Low',      c: '#1d4ed8', bg: '#dbeafe' },
                { n: summary.pass     || 0, l: 'Pass',     c: '#15803d', bg: '#dcfce7' },
              ].map(({ n, l, c, bg }) => (
                <div key={l} style={{ background: bg, borderRadius: '5px', padding: '8px 12px', textAlign: 'center', minWidth: '70px' }}>
                  <div style={{ fontSize: '20px', fontWeight: 800, color: '#111', fontFamily: "'Space Mono', monospace" }}>{n}</div>
                  <div style={{ fontSize: '10px', fontWeight: 700, color: c, textTransform: 'uppercase' }}>{l}</div>
                </div>
              ))}
            </div>
            <div style={{ fontSize: '12px', color: '#888', fontFamily: "'Space Mono', monospace", lineHeight: 1.8 }}>
              <div>Target: {url}</div>
              <div>Scanned: {new Date().toLocaleString()}</div>
              <div>Duration: {scanDuration} · {results.length} checks run</div>
            </div>
          </div>
        </div>

        {/* Category scores */}
        <div style={{ background: '#fff', border: '1px solid #ddd', borderRadius: '8px', padding: '20px 24px', marginBottom: '16px' }}>
          <div style={{ display: 'flex', alignItems: 'center', justifyContent: 'space-between', marginBottom: '16px', flexWrap: 'wrap', gap: '8px' }}>
            <h3 style={{ fontSize: '14px', fontWeight: 700, color: '#111' }}>Category Scores</h3>
            <span style={{ fontSize: '11px', color: '#888', fontFamily: "'Space Mono', monospace" }}>weighted average • higher weight = more impact</span>
          </div>
          <div style={{ display: 'grid', gridTemplateColumns: 'repeat(auto-fill, minmax(240px, 1fr))', gap: '10px' }}>
            {Object.entries(categoryScores).map(([cat, sc]) => {
              const catColor = sc >= 75 ? '#16a34a' : sc >= 55 ? '#d97706' : '#e02929';
              const catBg = sc >= 75 ? '#f0fdf4' : sc >= 55 ? '#fffbeb' : '#fff8f8';
              const catBorder = sc >= 75 ? '#bbf7d0' : sc >= 55 ? '#fde68a' : '#fecdd3';
              return (
                <div key={cat} style={{ background: catBg, border: `1px solid ${catBorder}`, borderRadius: '6px', padding: '12px 14px' }}>
                  <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', marginBottom: '8px' }}>
                    <div style={{ display: 'flex', alignItems: 'center', gap: '6px' }}>
                      <span>{CAT_ICONS[cat] || '🔍'}</span>
                      <span style={{ fontSize: '12px', color: '#444', fontWeight: 700 }}>{cat}</span>
                    </div>
                    <span style={{ fontSize: '15px', fontWeight: 800, color: catColor, fontFamily: "'Space Mono', monospace" }}>{sc}%</span>
                  </div>
                  <div style={{ height: '6px', background: 'rgba(0,0,0,0.08)', borderRadius: '3px', overflow: 'hidden' }}>
                    <div style={{ height: '100%', width: `${sc}%`, background: catColor, borderRadius: '3px', transition: 'width 1s ease' }} />
                  </div>
                  <div style={{ marginTop: '5px', fontSize: '10px', color: '#999', fontFamily: "'Space Mono', monospace" }}>weight: {CAT_WEIGHTS[cat] || '?'} pts</div>
                </div>
              );
            })}
          </div>
          <div style={{ marginTop: '12px', padding: '10px 14px', background: '#f8fafc', border: '1px solid #e2e8f0', borderRadius: '5px', fontSize: '11px', color: '#64748b', fontFamily: "'Space Mono', monospace" }}>
            ℹ️  Scoring: PASS = full points · WARNING = 50% · FAIL = 0%. Overall score = weighted average of all 25 checks. ERROR checks are excluded.
          </div>
        </div>

        {/* Attack vectors warning */}
        {score < 60 && topAttackVectors && topAttackVectors.length > 0 && (
          <div style={{ background: '#fff8f8', border: '1px solid #fecdd3', borderRadius: '8px', padding: '18px 22px', marginBottom: '16px' }}>
            <div style={{ fontWeight: 700, color: '#b91c1c', marginBottom: '10px', fontSize: '14px' }}>
              ⚠ Likely attack paths based on these findings:
            </div>
            <div style={{ display: 'flex', flexDirection: 'column', gap: '7px' }}>
              {topAttackVectors.map((v, i) => (
                <div key={i} style={{ display: 'flex', gap: '10px', alignItems: 'flex-start', fontSize: '13px', color: '#7f1d1d' }}>
                  <span style={{ fontWeight: 700, flexShrink: 0, color: '#e02929' }}>{i+1}.</span>
                  <span>{v}</span>
                </div>
              ))}
            </div>
          </div>
        )}

        {/* Findings */}
        <div style={{ background: '#fff', border: '1px solid #ddd', borderRadius: '8px', padding: '20px 24px' }}>
          <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', marginBottom: '16px', flexWrap: 'wrap', gap: '10px' }}>
            <h3 style={{ fontSize: '14px', fontWeight: 700, color: '#111' }}>All Findings</h3>
            <div style={{ display: 'flex', gap: '6px', flexWrap: 'wrap' }}>
              {[
                { id: 'all',      label: `All (${results.length})` },
                { id: 'issues',   label: `Issues (${(summary.critical||0)+(summary.high||0)+(summary.medium||0)+(summary.low||0)})` },
                { id: 'critical', label: `Critical (${summary.critical||0})` },
                { id: 'pass',     label: `Pass (${summary.pass||0})` },
              ].map(btn => (
                <button
                  key={btn.id}
                  onClick={() => setFilter(btn.id)}
                  style={{
                    background: filter === btn.id ? '#1c1c1c' : '#fff',
                    color: filter === btn.id ? '#fff' : '#555',
                    border: `1px solid ${filter === btn.id ? '#1c1c1c' : '#ddd'}`,
                    borderRadius: '4px',
                    padding: '4px 12px',
                    fontSize: '12px',
                    cursor: 'pointer',
                    fontWeight: 600,
                  }}
                >
                  {btn.label}
                </button>
              ))}
            </div>
          </div>

          <div>
            {filtered.map((r, i) => <VulnCard key={r.checkId || i} result={r} index={i} />)}
            {filtered.length === 0 && (
              <div style={{ textAlign: 'center', padding: '32px', color: '#aaa', fontSize: '14px' }}>
                Nothing matches this filter.
              </div>
            )}
          </div>
        </div>

        {/* Footer */}
        <div style={{ textAlign: 'center', marginTop: '28px', paddingBottom: '20px' }}>
          <button
            onClick={generateReport}
            style={{ background: '#e02929', color: '#fff', border: 'none', borderRadius: '6px', padding: '10px 32px', fontSize: '14px', fontWeight: 600, cursor: 'pointer' }}
          >
            ↓ Download full report (.txt)
          </button>
          <p style={{ marginTop: '10px', fontSize: '12px', color: '#bbb' }}>ShieldScan · for authorized security testing only</p>
        </div>

      </div>
    </div>
  );
}
